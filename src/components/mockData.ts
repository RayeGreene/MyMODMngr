export const getFilteredMods = (
  mods: Mod[],
  searchQuery: string,
  category: string,
  tags: string[],
  sortBy: string,
  selectedCharacters?: string[]
): Mod[] => {
  let filtered = mods;

  // Filter by search query
  if (searchQuery) {
    const query = searchQuery.toLowerCase();
    filtered = filtered.filter(
      (mod) =>
        mod.name.toLowerCase().includes(query) ||
        mod.description.toLowerCase().includes(query) ||
        mod.author.toLowerCase().includes(query) ||
        mod.tags.some((tag) => tag.toLowerCase().includes(query))
    );
  }

  // Filter by category
  if (category !== "all") {
    filtered = filtered.filter((mod) => mod.category === category);
  }

  // Filter by selected characters (when in characters category)
  if (selectedCharacters && selectedCharacters.length > 0) {
    filtered = filtered.filter(
      (mod) => mod.character && selectedCharacters.includes(mod.character)
    );
  }

  // Filter by tags
  if (tags.length > 0) {
    filtered = filtered.filter((mod) =>
      tags.every((tag) => mod.tags.includes(tag))
    );
  }

  // Sort
  switch (sortBy) {
    case "Popular":
      filtered.sort((a, b) => b.downloads - a.downloads);
      break;
    case "Recent":
      filtered.sort(
        (a, b) =>
          new Date(b.lastUpdated).getTime() - new Date(a.lastUpdated).getTime()
      );
      break;
    case "Rating":
      filtered.sort((a, b) => b.rating - a.rating);
      break;
    case "Downloads":
      filtered.sort((a, b) => b.downloads - a.downloads);
      break;
    default:
      break;
  }

  return filtered;
};

// Get unique characters from mods
export const getAvailableCharacters = (mods: Mod[]): string[] => {
  const characters = mods
    .filter((mod) => mod.category === "characters" && mod.character)
    .map((mod) => mod.character!)
    .filter((character, index, array) => array.indexOf(character) === index)
    .sort();

  return characters;
};
